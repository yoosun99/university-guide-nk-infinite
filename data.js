// Initialize the application
document.addEventListener("DOMContentLoaded", () => {
  const universitySelect = document.getElementById("university")
  const departmentSelect = document.getElementById("department")
  const resultArea = document.getElementById("resultArea")

  // Populate university dropdown
  Object.keys(universityData).forEach((university) => {
    const option = document.createElement("option")
    option.value = university
    option.textContent = university
    universitySelect.appendChild(option)
  })

  // Handle university selection
  universitySelect.addEventListener("change", function () {
    const selectedUniversity = this.value
    departmentSelect.innerHTML = '<option value="">학과/계열을 선택하세요</option>'
    resultArea.innerHTML = "대학교와 학과를 선택하시면 권장 이수 과목이 표시됩니다"
    resultArea.className = "result-area empty"

    if (selectedUniversity) {
      departmentSelect.disabled = false
      const departments = universityData[selectedUniversity].departments

      Object.keys(departments).forEach((dept) => {
        const option = document.createElement("option")
        option.value = dept
        option.textContent = dept
        departmentSelect.appendChild(option)
      })
    } else {
      departmentSelect.disabled = true
    }
  })

  // Handle department selection
  departmentSelect.addEventListener("change", function () {
    const selectedUniversity = universitySelect.value
    const selectedDepartment = this.value

    if (selectedUniversity && selectedDepartment) {
      displayResult(selectedUniversity, selectedDepartment)
    } else {
      resultArea.innerHTML = "대학교와 학과를 선택하시면 권장 이수 과목이 표시됩니다"
      resultArea.className = "result-area empty"
    }
  })

  function displayResult(university, department) {
    const data = universityData[university]
    const deptData = data.departments[department]

    resultArea.className = "result-area"

    let html = `<div class="result-title">${university} - ${department}</div>`

    // Display university notices
    if (data.안내사항 && data.안내사항.length > 0) {
      html += '<div class="info-section">'
      html += "<h3>대학별 안내사항</h3>"
      html += '<div class="info-content">'
      data.안내사항.forEach((notice) => {
        html += `<p>• ${notice}</p>`
      })
      html += "</div></div>"
    }

    // Display recommended courses
    if (deptData.권장과목) {
      html += '<div class="info-section">'
      html += "<h3>권장 이수 과목</h3>"
      html += '<div class="info-content">'

      if (typeof deptData.권장과목 === "string") {
        if (deptData.권장과목 === "권장과목 없음" || deptData.권장과목.includes("권장 이수과목 없음")) {
          html += `<div style="background: #f8f9fa; padding: 25px; border-radius: 12px; text-align: center; color: #666; font-size: 16px; border: 2px dashed #dee2e6; margin-top: 10px;">
                     <span style="font-size: 20px; margin-right: 8px;">📌</span>
                     <strong>${deptData.권장과목}</strong>
                     <div style="margin-top: 10px; font-size: 14px; color: #868e96;">학생의 진로와 적성에 따라 자유롭게 선택하세요</div>
                   </div>`
        } else {
          html += `<p>${deptData.권장과목}</p>`
        }
      } else {
        for (const [category, courses] of Object.entries(deptData.권장과목)) {
          html += `<p><strong>${category}:</strong> ${courses}</p>`
        }
      }
      html += "</div></div>"
    }

    // Display core subjects if available
    if (deptData.핵심과목) {
      html += '<div class="info-section">'
      html += "<h3>핵심과목</h3>"
      html += '<div class="info-content">'
      if (typeof deptData.핵심과목 === "string") {
        if (deptData.핵심과목 === "권장과목 없음") {
          html += `<div style="background: #f8f9fa; padding: 25px; border-radius: 12px; text-align: center; color: #666; font-size: 16px; border: 2px dashed #dee2e6; margin-top: 10px;">
                     <span style="font-size: 20px; margin-right: 8px;">📌</span>
                     <strong>${deptData.핵심과목}</strong>
                     <div style="margin-top: 10px; font-size: 14px; color: #868e96;">학생의 진로와 적성에 따라 자유롭게 선택하세요</div>
                   </div>`
        } else {
          html += `<p>${deptData.핵심과목}</p>`
        }
      } else {
        for (const [category, courses] of Object.entries(deptData.핵심과목)) {
          html += `<p><strong>${category}:</strong> ${courses}</p>`
        }
      }
      html += "</div></div>"
    }

    // Display recommended subjects if available
    if (deptData.권장과목_추가) {
      html += '<div class="info-section">'
      html += "<h3>권장과목 (추가)</h3>"
      html += '<div class="info-content">'
      if (typeof deptData.권장과목_추가 === "string") {
        if (deptData.권장과목_추가 === "권장과목 없음") {
          html += `<div style="background: #f8f9fa; padding: 25px; border-radius: 12px; text-align: center; color: #666; font-size: 16px; border: 2px dashed #dee2e6; margin-top: 10px;">
                     <span style="font-size: 20px; margin-right: 8px;">📌</span>
                     <strong>${deptData.권장과목_추가}</strong>
                     <div style="margin-top: 10px; font-size: 14px; color: #868e96;">학생의 진로와 적성에 따라 자유롭게 선택하세요</div>
                   </div>`
        } else {
          html += `<p>${deptData.권장과목_추가}</p>`
        }
      } else {
        for (const [category, courses] of Object.entries(deptData.권장과목_추가)) {
          html += `<p><strong>${category}:</strong> ${courses}</p>`
        }
      }
      html += "</div></div>"
    }

    // Display special notices if available
    if (deptData.특별안내) {
      html += '<div class="info-section">'
      html += "<h3>특별 안내</h3>"
      html += '<div class="info-content">'
      for (const [key, value] of Object.entries(deptData.특별안내)) {
        html += `<p><strong>${key}:</strong> ${value}</p>`
      }
      html += "</div></div>"
    }

    // Display KU:PICK if available
    if (deptData["KU:PICK"]) {
      html += '<div class="info-section">'
      html += "<h3>KU:PICK (이수 추천과목)</h3>"
      html += '<div class="info-content">'
      html += `<p>${deptData["KU:PICK"]}</p>`
      html += "</div></div>"
    }

    // Display core competencies if available
    if (deptData.핵심역량) {
      html += '<div class="info-section">'
      html += "<h3>핵심역량</h3>"
      html += '<div class="info-content">'
      html += `<p>${deptData.핵심역량}</p>`
      html += "</div></div>"
    }

    resultArea.innerHTML = html
  }
})
